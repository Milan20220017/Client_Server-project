    private void jButtonAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAddActionPerformed
ProfessionalDegree pd = (ProfessionalDegree) cmbDegree.getSelectedItem();
Worker w = (Worker) cmbWorker.getSelectedItem();

if (pd == null || w == null) {
    JOptionPane.showMessageDialog(this, "Both worker and degree must be selected", "ERROR", JOptionPane.ERROR_MESSAGE);
    return;
}

List<WrkPD> listOfWrkpd = new ArrayList<>();
w.setWs(listOfWrkpd);
WrkPD ws = new WrkPD(w, pd, LocalDate.now());

try {
    w.getWs().add(ws);
    int isAffected = ClientControllerWorker.getInstance().addWS(w);
    if (isAffected != 0) {
        fillData();
        JOptionPane.showMessageDialog(this, "System saved professionalDegree", "System", JOptionPane.INFORMATION_MESSAGE);
    } else {
        JOptionPane.showMessageDialog(this, "System did not save professionalDegree", "Error", JOptionPane.ERROR_MESSAGE);
    }
} catch (Exception ex) {
    JOptionPane.showMessageDialog(this, "Sistem ne može da zapamti stručnu spremu", "Error", JOptionPane.ERROR_MESSAGE);
    Logger.getLogger(AddWS.class.getName()).log(Level.SEVERE, null, ex);
}

    }//GEN-LAST:event_jButtonAddActionPerformed



