    private void fillData() {
        try {
            List<AbstractDomainObject> w = ClientControllerWorker.getInstance().getWorkers(null);
            System.out.println(w);
            cmbWorker.setModel(new DefaultComboBoxModel(w.toArray()));
        } catch (Exception ex) {
            Logger.getLogger(SearchWorker.class.getName()).log(Level.SEVERE, null, ex);
        }

        try {
            List<AbstractDomainObject> pd = ClientControllerProfessionalDegree.getInstance().getProfessionalDegree(null);
//            System.out.println(pd);
            cmbDegree.setModel(new DefaultComboBoxModel(pd.toArray()));
        } catch (Exception ex) {
            Logger.getLogger(SearchWorker.class.getName()).log(Level.SEVERE, null, ex);
        }
        cmbDegree.setSelectedIndex(-1);
        cmbWorker.setSelectedIndex(-1);

    }

