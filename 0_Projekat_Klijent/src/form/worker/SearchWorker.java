    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        if (tbl.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(this, "Please select customer", "ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        }
        Worker c = (Worker) ((TableModel) tbl.getModel()).get(tbl.getSelectedRow());

        int odgovor = JOptionPane.showConfirmDialog(this, "Delete Worker :" + c, "Are you shure", JOptionPane.YES_NO_OPTION);
        if (odgovor == JOptionPane.NO_OPTION) {
            return;
        }

        try {
           boolean isDeleted= ClientControllerWorker.getInstance().deleteWorker(c);
             if(!isDeleted){
                 JOptionPane.showMessageDialog(this, "You have to delete all receipts connected to that customer first", "Error",JOptionPane.ERROR_MESSAGE );

            }
            else{
                allWorkers.remove(c);
                tbl.setModel(new TableModel(allWorkers));
            JOptionPane.showMessageDialog(this, "You successfully deleted customer", "Successful", JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (Exception ex) {
            Logger.getLogger(SearchCustomer.class.getName()).log(Level.SEVERE, null, ex);
        }
        fillData();

    }//GEN-LAST:event_btnDeleteActionPerformed

