    public void fillData() {
        try {
            allWorkers= ClientControllerWorker.getInstance().getWorkers(null);
            tbl.setModel(new TableModel(allWorkers));
        } catch (Exception ex) {
            Logger.getLogger(SearchWorker.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

    private void addListener() {
        txtWorker.getDocument().addDocumentListener(new DocumentListener() {
            @Override
            public void insertUpdate(DocumentEvent e) {
                updateWorker();
            }

            @Override
            public void removeUpdate(DocumentEvent e) {
                updateWorker();
            }

            @Override
            public void changedUpdate(DocumentEvent e) {
                throw new UnsupportedOperationException("Not supported yet."); // Generated from nbfs://nbhost/SystemFileSystem/Templates/Classes/Code/GeneratedMethodBody
            }

            private void updateWorker() {
                try {
                    List<AbstractDomainObject> w = ClientControllerWorker.getInstance().getWorkers(txtWorker.getText());
                    tbl.setModel(new TableModel(w));
                } catch (Exception ex) {
                    Logger.getLogger(SearchCustomer.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        });
    }

