    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed

        String name = txtName.getText();
        String lastname = txtLastname.getText();
        String phone = txtPhone.getText();
        City city = (City) cmbCity.getSelectedItem();
       if (!phone.matches("\\d+")) {  // \d znači cifra, + znači jedan ili više puta
    JOptionPane.showMessageDialog(rootPane, "Phone number must contain only digits", "System", JOptionPane.ERROR_MESSAGE);
    return;
}
        if (name == null || !name.matches("[a-zA-Z]+") || lastname == null || !lastname.matches("[a-zA-Z]+") || name.isEmpty() || lastname.isEmpty() || phone == null || phone.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "The system cannot save the customer", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (city == null) {
    JOptionPane.showMessageDialog(rootPane, "Please select a city", "System", JOptionPane.ERROR_MESSAGE);
    return;
}

//        Customer c = new Customer(name, lastname, phone, city);
Customer c=new Customer(0, name, lastname, phone, city);

        try {
            c.setCustomerId(ClientControllerCustomer.getInstance().addCustomer(c));
            JOptionPane.showMessageDialog(this, "System saved the customer", "", JOptionPane.INFORMATION_MESSAGE);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "ERROR", JOptionPane.ERROR_MESSAGE);
        }

        if (mod == 1 || mod == 2) {
            ((SearchCustomer) parent).fillData();
        }
        if (mod == 3) {
            ((AddReceipt) parent).setCmb(customer);
        }
        dispose();

    }//GEN-LAST:event_btnAddActionPerformed

    private void btnChangeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnChangeActionPerformed

        String name = txtName.getText();
        String lastname = txtLastname.getText();
        String phone = txtPhone.getText();
        City city = (City) cmbCity.getSelectedItem();

        if (name == null || !name.matches("[a-zA-Z]+") || lastname == null || !lastname.matches("[a-zA-Z]+") || name.isEmpty() || lastname.isEmpty() || phone == null || phone.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "System has saved the customer", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        customer.setFirstName(txtName.getText());
        customer.setLastName(txtLastname.getText());
        customer.setPhoneNumber(txtPhone.getText());

        customer.setCity((City) cmbCity.getSelectedItem());

        try {
            ClientControllerCustomer.getInstance().updateCustomer(customer);
            JOptionPane.showMessageDialog(this, "System has saved the customer", "Done", JOptionPane.INFORMATION_MESSAGE);
        } catch (Exception ex) {
            Logger.getLogger(AddCustomer.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(rootPane, "System did not save the customer", "Error", JOptionPane.ERROR_MESSAGE);

        }
        ((SearchCustomer) parent).fillData();
        dispose();
    }//GEN-LAST:event_btnChangeActionPerformed

