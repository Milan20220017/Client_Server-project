    public void fillData() {
        try {
            List<AbstractDomainObject> c = ClientControllerCity.getInstance().getCities();
            cmbCity.setModel(new DefaultComboBoxModel(c.toArray()));
        } catch (Exception ex) {
            Logger.getLogger(AddCustomer.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    public void fillCustomer() {
        txtName.setText(customer.getFirstName());
        txtLastname.setText(customer.getLastName());
        txtPhone.setText(customer.getPhoneNumber());
        cmbCity.setSelectedItem(customer.getCity());
    }

