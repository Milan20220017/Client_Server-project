    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        if (tbl.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(this, "Please select customer", "ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        }
        Customer c = (Customer) ((TableModel) tbl.getModel()).get(tbl.getSelectedRow());
        if(c==null){
            JOptionPane.showMessageDialog(this, "System can not find the customer", "Error", JOptionPane.ERROR_MESSAGE);
        }
        else{
            
        AddCustomer ad = new AddCustomer(this, c);
        ad.setVisible(true);
        ad.setLocationRelativeTo(null);
        JOptionPane.showMessageDialog(null, "System found the customer", "info", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
     if (tbl.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(this, "Please select row", "ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        }
        Customer customer = (Customer) ((TableModel) (tbl.getModel())).get(tbl.getSelectedRow());
        try {
            boolean deletedSuccessful=ClientControllerCustomer.getInstance().deleteCustomer(customer);
            if(!deletedSuccessful){
                 JOptionPane.showMessageDialog(this, "You have to delete all receipts connected to that customer first", "Error",JOptionPane.ERROR_MESSAGE );

            }
            else{
                allCustomers.remove(customer);
                
                tbl.setModel(new TableModel(allCustomers));
            JOptionPane.showMessageDialog(this, "The system has successfully deleted the customer.", "Success", JOptionPane.INFORMATION_MESSAGE);
            } 
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "The system could not delete the customer.", "Error", JOptionPane.ERROR_MESSAGE);
            }
    }//GEN-LAST:event_btnDeleteActionPerformed

