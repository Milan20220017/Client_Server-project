    private void updateCustomer() {
        String condition=txtSearch.getText();
        System.out.println("Usao u update");
        City cityFromcm=(City) cmbCity.getSelectedItem();
        try {
Customer c=new Customer(0, txtSearch.getText().trim(), null, null, null);
            allCustomers = ClientControllerCustomer.getInstance().getCustomers(condition);
            if (cmbCity.getSelectedIndex() != -1) {
                c.setCity((City) cmbCity.getSelectedItem());
            } 
            List<AbstractDomainObject> forTable=new ArrayList<>();
            if(cityFromcm!=null){
            for(AbstractDomainObject ado: allCustomers){
                Customer cus=(Customer) ado;
                if(cus.getCity().getCityId()==cityFromcm.getCityId()){
                    forTable.add(ado);
                }
            }
            lblupdate(!forTable.isEmpty());
            tbl.setModel(new TableModel(forTable));
            }
            else{
            lblupdate(!allCustomers.isEmpty());

            tbl.setModel(new TableModel(allCustomers));
}
        } catch (Exception ex) {
            Logger.getLogger(SearchCustomer.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

 private void lblupdate(boolean found) {
    if (found) {
        lblNasao.setText("The system has found customers matching the given criteria");
        lblNasao.setFont(new Font("Arial", Font.BOLD, 14));
        lblNasao.setForeground(new Color(0, 128, 0)); // green
    } else {
        lblNasao.setText("The system could not find customers matching the given criteria");
        lblNasao.setFont(new Font("Arial", Font.BOLD, 14));
        lblNasao.setForeground(new Color(128, 0, 30)); // dark red
    }
}

