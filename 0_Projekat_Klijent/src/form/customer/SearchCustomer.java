    public void fillData() {
        try { 
            
            allCustomers = ClientControllerCustomer.getInstance().getCustomers(null);
            lblupdate(!allCustomers.isEmpty());
            tbl.setModel(new TableModel(allCustomers));

            List<AbstractDomainObject> ci = ClientControllerCity.getInstance().getCities();
            cmbCity.setModel(new DefaultComboBoxModel(ci.toArray()));
            cmbCity.setSelectedIndex(-1);

        } catch (Exception ex) {
            Logger.getLogger(SearchCustomer.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

