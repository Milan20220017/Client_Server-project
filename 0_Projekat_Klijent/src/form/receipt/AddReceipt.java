    private void jButtonDoneActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDoneActionPerformed
     if (cmbCustomer.getSelectedIndex() == -1) {
            JOptionPane.showMessageDialog(this, "You must select a customer", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (items.isEmpty()) {
            JOptionPane.showMessageDialog(this, "System can not save the receipt", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        LocalDate date = LocalDate.now();
        Customer selectedCustomer = (Customer) cmbCustomer.getSelectedItem();
        List<ReceiptItem> receiptItems = new ArrayList<>();
        for (AbstractDomainObject i : items) {
            receiptItems.add((ReceiptItem) i);
        }
        double totalAmount = 0;
        for (ReceiptItem item : receiptItems) {
            if (item.getPrice() < 0 || item.getQuantity() < 0 || item.getValue() < 0) {
                JOptionPane.showMessageDialog(this, "Price, quantity or value cannot be less than zero", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            totalAmount += item.getValue();
        }

        Receipt receipt = new Receipt(0, date, totalAmount, worker, selectedCustomer, receiptItems);

        try {
            ClientControllerReceipt.getInstance().addReceipt(receipt);
            JOptionPane.showMessageDialog(this, "Receipt has been saved", "Success", JOptionPane.INFORMATION_MESSAGE);
            dispose();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonDoneActionPerformed

