    private void fillData() {
        lblWorker.setText(worker.getFirstName()+ " " + worker.getLastName());
        
        fillCmb();

        try {

            paintings = ClientControllerPainting.getInstance().getPaintings();

        } catch (Exception ex) {
            Logger.getLogger(AddReceipt.class.getName()).log(Level.SEVERE, null, ex);
        }

        tblPainting.setModel(new TableModel(paintings));

        tblItems.setModel(new TableModel(items));

    }

    private void updateTblItems() {
        tblItems.setModel(new TableModel(items));
        updateLbls();

    }

    private void updateLbls() {
               double totalPrice = 0;
        for (AbstractDomainObject i : items) {
            totalPrice += ((ReceiptItem) (i)).getPrice()* ((ReceiptItem) (i)).getQuantity();
        }

        lblPrice.setText(totalPrice + "");
    }

    private ReceiptItem containsPainting(Painting painting) {
        for (AbstractDomainObject j : items) {
            if (((ReceiptItem) j).getPainting().getPaintingId()== painting.getPaintingId()) {
                return (ReceiptItem) j;
            }
        }
        return null;
    }

