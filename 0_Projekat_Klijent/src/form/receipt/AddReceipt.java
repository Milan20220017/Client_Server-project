    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        if (tblPainting.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(this, "You must select item", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
       Painting painting = (Painting) ((TableModel) (tblPainting.getModel())).get(tblPainting.getSelectedRow());

        ReceiptItem i = containsPainting(painting);
        if (i != null) {
            i.setQuantity(i.getQuantity() + quantity);
            updateTblItems();
            updateLbls();
            txtQuantity.setText("1");
            quantity = 1;
            return;
        }
        rbr++;
            int quantiti=Integer.parseInt(txtQuantity.getText());
        ReceiptItem ri=new ReceiptItem(0,rbr, quantity, painting.getPrice(),quantiti*painting.getPrice(), painting);
  items.add(ri);
        updateTblItems();

        txtQuantity.setText("1");
        quantity = 1;

    }//GEN-LAST:event_jButton2ActionPerformed

