    public void fillData() {

        try {
            c = ClientControllerCustomer.getInstance().getCustomers(null);
        } catch (Exception ex) {
            Logger.getLogger(AddReceipt.class.getName()).log(Level.SEVERE, null, ex);
        }
        cmbCustomer.setModel(new DefaultComboBoxModel(c.toArray()));

        try {
            w = ClientControllerWorker.getInstance().getWorkers(null);
        } catch (Exception ex) {
            Logger.getLogger(SearchReceipt.class.getName()).log(Level.SEVERE, null, ex);
        }
        cmbWorker.setModel(new DefaultComboBoxModel(w.toArray()));

        try {
            d = ClientControllerPainting.getInstance().getPaintings();
        } catch (Exception ex) {
            Logger.getLogger(SearchReceipt.class.getName()).log(Level.SEVERE, null, ex);
        }
        cmbPainting.setModel(new DefaultComboBoxModel(d.toArray()));
        try {
            receiptForCb = ClientControllerReceipt.getInstance().getReceipts(null);
            for(Receipt rece: receiptForCb){
                receiptId.add(rece.getReceiptId());
            }
        } catch (Exception ex) {
            Logger.getLogger(SearchReceipt.class.getName()).log(Level.SEVERE, null, ex);
        }
        cmbReceipt.setModel(new DefaultComboBoxModel(receiptId.toArray()));
        cmbReceipt.setSelectedIndex(-1);
        cmbCustomer.setSelectedIndex(-1);
        cmbWorker.setSelectedIndex(-1);
        cmbPainting.setSelectedIndex(-1);

        try {
            ri = ClientControllerReceipt.getInstance().getReceipts(null);
            tbl.setModel(new TableModel(ri));
        } catch (Exception ex) {
            Logger.getLogger(SearchReceipt.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

    private void updateTable() {
        Worker w = null;
        Customer c = null;
        Painting d = null;
        int ReceiptID=0;
        if (cmbWorker.getSelectedIndex() != -1) {
            w = (Worker) cmbWorker.getSelectedItem();
        }
        if (cmbCustomer.getSelectedIndex() != -1) {
            c = (Customer) cmbCustomer.getSelectedItem();
        }
        if (cmbPainting.getSelectedIndex() != -1) {
            d = (Painting) cmbPainting.getSelectedItem();
        }
        if (cmbReceipt.getSelectedIndex() != -1) {
            ReceiptID = (int) cmbReceipt.getSelectedItem();
        }

        ReceiptItem receiptItem = new ReceiptItem(0, 0, 0, 0.0, 0.0,d);
        List<ReceiptItem> list = new ArrayList<>();
        list.add(receiptItem);

        Receipt receipt = new Receipt(ReceiptID, null, 0.0, w, c, list);

        try {
            ri= ClientControllerReceipt.getInstance().getReceipts(receipt);
            if(d!=null){
            List<Receipt> forShowing=new ArrayList<>();
            for(int i=0;i<ri.size();i++){
                for(int j=0;j<ri.get(i).getItemList().size();j++){
                    if(ri.get(i).getItemList().get(j).getPainting().getPaintingId()==(d.getPaintingId())){
                        forShowing.add(ri.get(i));
                        break;
                    }
                }
                lblupdate(!forShowing.isEmpty());
            tbl.setModel(new TableModel(forShowing));
            }
            }else{
            lblupdate(!ri.isEmpty());
            tbl.setModel(new TableModel(ri));
            }
        } catch (Exception ex) {
            Logger.getLogger(SearchReceipt.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }

