  public void updateReceipt() throws Exception {
       String tekst = txtReceipt.getText();
       if(tekst.isEmpty()){receiptForCb = ClientControllerReceipt.getInstance().getReceipts(null);
       receiptId.clear();
            for(Receipt rece: receiptForCb){
                System.out.println("Udje li ovdje");
                receiptId.add(rece.getReceiptId());
            }
                cmbReceipt.setModel(new DefaultComboBoxModel(receiptId.toArray()));
                cmbReceipt.setSelectedIndex(-1);
                return;
       }
try {
    int broj = Integer.parseInt(tekst);
    if(receiptId.contains(broj)){
        receiptId.clear();
        receiptId.add(broj);
        cmbReceipt.setModel(new DefaultComboBoxModel(receiptId.toArray()));
        cmbReceipt.setSelectedIndex(-1);
    }
    else{
        receiptId.clear();
    cmbReceipt.setModel(new DefaultComboBoxModel(receiptId.toArray()));
    }
    // Validan broj – možeš dalje da koristiš promenljivu `broj`
    System.out.println("Unet broj je: " + broj);
} catch (NumberFormatException e) {
    // Nije unet validan broj
    JOptionPane.showMessageDialog(null, "Molimo unesite validan ceo broj!");
    receiptId.clear();
    cmbReceipt.setModel(new DefaultComboBoxModel(receiptId.toArray()));
    cmbReceipt.setSelectedIndex(-1);
}

    }
