    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        try {                                        
            if (tbl.getSelectedRow() == -1) {
                JOptionPane.showMessageDialog(this, "You have to select receipt first", "ERROR", JOptionPane.ERROR_MESSAGE);
                return;
            }
            Receipt r = (Receipt) ((TableModel) (tbl.getModel())).get(tbl.getSelectedRow());
            try {
                ri = ClientControllerReceipt.getInstance().getReceipts(r);
                if (!ri.isEmpty()) {
                    
                    JOptionPane.showMessageDialog(this, "The system has found the receipt", "System", JOptionPane.INFORMATION_MESSAGE);
                    r = (Receipt) ri.get(0);
                } else {
                    JOptionPane.showMessageDialog(this, "The system cannot find the receipt", "Error", JOptionPane.ERROR_MESSAGE);
                    return;
                }
                
            } catch (Exception ex) {
                Logger.getLogger(SearchReceipt.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this, "The system cannot find the receipt", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            UpdateReceipt ur = new UpdateReceipt(this, r);
            ur.setVisible(true);
            ur.setLocationRelativeTo(null);
        } catch (Exception ex) {
               Logger.getLogger(SearchReceipt.class.getName()).log(Level.SEVERE, null, ex);
           }
    }//GEN-LAST:event_btnEditActionPerformed

