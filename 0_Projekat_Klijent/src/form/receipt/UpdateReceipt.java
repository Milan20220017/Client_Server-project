    private void jButtonEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditActionPerformed
        if (tbl.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(this, "Please select row", "ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        }
        ReceiptItem ri = (ReceiptItem) ((TableModel) tbl.getModel()).get(tbl.getSelectedRow());
        AddEdit ad = new AddEdit(this, ri);
        ad.setVisible(true);
        if(ad.isVisible()==true){
            JOptionPane.showMessageDialog(this, "System found the receipt", "ERROR", JOptionPane.INFORMATION_MESSAGE);
        }
        else{
            JOptionPane.showMessageDialog(this, "System can not find the receipt", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonEditActionPerformed

    private void jButtonDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeleteActionPerformed
        try {
            if (tbl.getSelectedRow() == -1) {
                JOptionPane.showMessageDialog(this, "Please select row", "ERROR", JOptionPane.ERROR_MESSAGE);
                return;
            }
            ReceiptItem stari = (ReceiptItem) ((TableModel) tbl.getModel()).get(tbl.getSelectedRow());
//            int affectedRow=ClientControllerReceipt.getInstance().removeReceiptItem(stari);
            receipt.getItemList().remove(stari);
            fillData();
        } catch (Exception ex) {
            Logger.getLogger(UpdateReceipt.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButtonDeleteActionPerformed

    private void jButtonDodajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDodajActionPerformed
       
        try {
            double totalAmount=0;
            for(ReceiptItem ritem: receipt.getItemList()){
                totalAmount+=ritem.getValue();
            }
            receipt.setTotalAmount(totalAmount);
            receipt.setDate(LocalDate.now());
            receipt.setCustomer((Customer) cmbCustomers.getSelectedItem());
            boolean b = ClientControllerReceipt.getInstance().update(receipt);
            parent.fillData();
            JOptionPane.showMessageDialog(this, "System save the receipt", "Sistem", JOptionPane.INFORMATION_MESSAGE);

            dispose();
        } catch (Exception ex) {
            Logger.getLogger(UpdateReceipt.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Sistem can not save the receipt", "ERROR", JOptionPane.ERROR_MESSAGE);

        }
    }//GEN-LAST:event_jButtonDodajActionPerformed

