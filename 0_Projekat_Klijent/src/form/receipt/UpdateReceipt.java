    private void fillData() {
        tbl.setModel(new TableModel(receipt.getItemList()));

    }

        public void addItem(ReceiptItem i) {

        if (receipt.getItemList().contains(i)) {
            fillData();
            return;
        }
i.setReceiptId(receipt.getReceiptId());
i.setItemNumber(receipt.getItemList().size());
        receipt.getItemList().add(i);
        fillData();
    }

    public void updateReceiptItem(ReceiptItem ri) {
    ri.setReceiptId(receipt.getReceiptId());
    List<ReceiptItem> itemList = receipt.getItemList();
    for (int i = 0; i < itemList.size(); i++) {
        if (itemList.get(i).getItemNumber() == ri.getItemNumber()) {
            itemList.set(i, ri);  
            break;
        }
    }
        fillData();
    }
    public void AddReceiptItem(ReceiptItem ri){
         ri.setReceiptId(receipt.getReceiptId());
        ri.setItemNumber(receipt.getItemList().size());
        receipt.getItemList().add(ri);
        fillData();
    }

private Customer findCustomer(List<AbstractDomainObject> allCustomers, int customerId) {
    for (AbstractDomainObject obj : allCustomers) {
        Customer c = (Customer) obj; 
        if (c.getCustomerId() == customerId) {
            return c;
        }
    }
    return null; // a
}

    private void fillCmb(List<AbstractDomainObject> allCustomers) {
        cmbCustomers.setModel(new DefaultComboBoxModel(allCustomers.toArray()));
        cmbCustomers.setSelectedItem(current);
        
    }
