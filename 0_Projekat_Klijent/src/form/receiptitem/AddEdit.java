    private void fillData() {

        List<AbstractDomainObject> d;
        try {
            d = ClientControllerPainting.getInstance().getPaintings();
            cmbPainting.setModel(new DefaultComboBoxModel(d.toArray()));
        } catch (Exception ex) {
            Logger.getLogger(AddEdit.class.getName()).log(Level.SEVERE, null, ex);
        }
        cmbPainting.setSelectedIndex(-1);

    }
//
    private void updateReceiptItem() {
        if (cmbPainting.getSelectedIndex() == -1) {
            return;
        }
        Painting painting = (Painting) cmbPainting.getSelectedItem();
        lblTotalPrice.setText(quantity*painting.getPrice()+"");
    }

