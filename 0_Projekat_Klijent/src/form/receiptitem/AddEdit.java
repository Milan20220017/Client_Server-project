    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed

        if (cmbPainting.getSelectedIndex() == -1) {
            return;
        }
        Painting selectedPainting=(Painting) cmbPainting.getSelectedItem();
        ri.setPainting(selectedPainting);
        ri.setPrice(selectedPainting.getPrice());
        ri.setQuantity(quantity);
        System.out.println(quantity+"OVo je kolicina ona ne radi");
        ri.setValue(quantity*selectedPainting.getPrice());
         System.out.println(ri.getItemNumber()+"Ovo je id nove stavke koji bi trebalo da je 0");
        parent.AddReceiptItem(ri);
        dispose();
    }//GEN-LAST:event_btnAddActionPerformed

